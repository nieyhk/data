FOREIGN KEY(진료번호) REFERENCES 진료(번호),
FOREIGN KEY(의사번호) REFERENCES 의사(번호),
FOREIGN KEY(환자번호) REFERENCES 환자(번호)
);


CREATE TABLE 수술실(
번호			INTEGER		NOT NULL,
수술번호		INTEGER		NOT NULL,
