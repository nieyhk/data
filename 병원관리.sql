진료번호		INTEGER		NOT NULL,
의사번호		INTEGER		NOT NULL,
환자번호		INTEGER		NOT NULL,
직원번호		INTEGER		NOT NULL,
차트위치		VARCHAR(50)	NOT NULL,
PRIMARY KEY(번호),
FOREIGN KEY(진료번호) REFERENCES 진료(번호),
FOREIGN KEY(의사번호) REFERENCES 의사(번호),
FOREIGN KEY(환자번호) REFERENCES 환자(번호),
FOREIGN KEY(직원번호) REFERENCES 직원(번호)
);


CREATE TABLE 제약회사(
번호			INTEGER			NOT NULL,
이름			VARCHAR(30)		NOT NULL,
전화번호		VARCHAR(15)		NOT NULL,
주소			VARCHAR(120)	NOT NULL,
PRIMARY KEY(번호)
);

CREATE TABLE 약(
번호			INTEGER			NOT NULL,
진료번호		INTEGER			NOT NULL,
의사번호		INTEGER			NOT NULL,
환자번호		INTEGER			NOT NULL,
이름			VARCHAR(50)		NOT NULL,
용도			VARCHAR(500)	NOT NULL,
금액			VARCHAR(20)		NOT NULL,
제약회사이름	VARCHAR(30)		NOT NULL,
PRIMARY KEY(번호),
FOREIGN KEY(진료번호) REFERENCES 진료(번호),
FOREIGN KEY(의사번호) REFERENCES 의사(번호),
FOREIGN KEY(환자번호) REFERENCES 환자(번호)
);


CREATE TABLE 수술(
번호			INTEGER		NOT NULL,
진료번호		INTEGER		NOT NULL,
의사번호		INTEGER		NOT NULL,
환자번호		INTEGER		NOT NULL,
시작시간		INTEGER		NOT NULL,
시간			INTEGER		NOT NULL,
PRIMARY KEY(번호),
FOREIGN KEY(진료번호) REFERENCES 진료(번호),
FOREIGN KEY(의사번호) REFERENCES 의사(번호),
FOREIGN KEY(환자번호) REFERENCES 환자(번호)
);


CREATE TABLE 입원(
