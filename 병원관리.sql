CREATE TABLE 환자(
번호			INTEGER			NOT NULL,
이름			VARCHAR(20)		NOT NULL,
주민등록번호	VARCHAR(15)		NOT NULL,
성별			CHAR(1)			NULL,
주소			VARCHAR(120)	NOT NULL,
전화번호		VARCHAR(15)		NOT NULL,
PRIMARY KEY(번호)
);


CREATE TABLE 직원(
번호			INTEGER			NOT NULL,
이름			VARCHAR(20)		NOT NULL,
성별			CHAR(1)			NULL,
주민등록번호	VARCHAR(15)		NOT NULL,
주소			VARCHAR(120)	NOT NULL,
담당부서		VARCHAR(10)		NOT NULL,
PRIMARY KEY(번호)
);


CREATE TABLE 진료(
번호			INTEGER			NOT NULL,
환자번호		INTEGER			NOT NULL,
의사번호		INTEGER			NOT NULL,
내용			VARCHAR(500)	NOT NULL,
일자			DATE			NOT NULL,
